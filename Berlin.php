        world on public display. Culturally minded visitors from all corners of the
        globe come to Berlin to see performances by leading orchestras – such as
        the internationally celebrated Berlin Philharmonic – and to attend ballets
        and operas at the three major opera houses. A multitude of theatres
        specialising in plays, variety, revue and cabaret offer stage entertainment
        in all its forms.

        The famous Kurfürstendamm, grand old Friedrichstrasse and the independent
        boutiques in and around the Hackesche Höfe offer endless scope for shopping.

        Nowadays, people look to Berlin for the latest trends in lifestyle, music
        and art. Inspired by this outpouring of creativity, growing numbers of
        artists are arriving in the city from around the world, making it one of
        Europe's most exciting destinations.

        A feature of Berlin, aside from its exuberance, is the way it continuously
        reinvents itself.

        But Berlin also has a relaxed vibe and lots of open spaces for a breath
        of fresh air. No city in Germany is greener than Berlin with its forests,
        lakes and sprawling parks. This laid-back way of life can be found throughout
        the city. When the weather gets warmer, life in Berlin moves outdoors to
        the beach bars, pavement cafés and open-air cinemas and theatres – perfect
        for enjoying the sunshine and the balmy summer nights.</p>

