        <h3>About Istanbul</h3>
        <p>The only city in the world that can lay claim to straddling two continents,
          Istanbul—once known as Constantinople, capital of the Byzantine and then
          the Ottoman Empire—has for centuries been a bustling metropolis with
          one foot in Europe and the other in Asia. Istanbul embraces this enviable
          position with both a certain chaos and inventiveness, ever evolving as
          one of the world’s most cosmopolitan crossroads.

          It’s often said that Istanbul is the meeting point of East and West,
          but visitors to this city built over the former capital of two great
          empires are likely to be just as impressed by the juxtaposition of old
          and new. Office towers creep up behind historic palaces, women in chic
          designer outfits pass others wearing long skirts and head coverings
          (sometimes chic and designer as well), peddlers’ pushcarts vie with
          battered old Fiats and shiny BMWs for dominance of the noisy, narrow
          streets, and the Grand Bazaar competes with modern shopping malls. At
          dawn, when the muezzin's call to prayer resounds from ancient minarets,
          there are inevitably a few hearty revelers still making their way home
          from nightclubs and bars.

          Most visitors to this sprawling city of more than 14 million will first
          set foot in the relatively compact Old City, where the legacy of the
          Byzantine and Ottoman empires can be seen in monumental works of
          architecture like the brilliant Aya Sofya and the beautifully proportioned
          mosques built by the great architect Sinan. Though it would be easy to
          spend days, if not weeks, exploring the wealth of attractions in the
          historical peninsula, visitors should make sure also to venture elsewhere
          in order to experience the vibrancy of contemporary Istanbul. With a
          lively nightlife propelled by its young population and an exciting arts
          scene that’s increasingly on the international radar, Istanbul is truly
          a city that never sleeps. It’s also a place where visitors will feel
          welcome: Istanbul may be on the Bosphorus, but at heart it’s a Mediterranean
          city, whose friendly inhabitants are effusively social and eager to
          share what they love most about it.</p>

