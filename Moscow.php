        ineffective at combatting this trend.

        As well as being a thriving economic centre (boasting more billionaires
        than any other city in the world), Moscow is also a centre of the arts.
        The city enjoyed a golden age of literature in the 19th century when Puskhin,
        Chekhov, Gogol, Turgenev, Dostoevsky and Tolstoy all worked here. Today
        the world-famous Bolshoi Ballet and Opera Company still perform, as well
        as a number of reputable theatre groups.

        Moscow has a number of competitive football teams, including Dynamo, CSKA,
        Lokomotiv and Spartak, all of whom have played on the European stage. It
        also hosted the 1980 Olympic Games, which were boycotted by many countries
        due to the Soviet invasion of Afghanistan. Moscow however lost out to
        London in its bid for the 2012 Olympics.</p>

