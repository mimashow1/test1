      <div id="city-about">
        <h3>About Paris</h3>
        <p>For centuries Paris has been one of the world’s most important and
          attractive cities. It is appreciated for the opportunities it offers
          for business and commerce, for study, for culture, and for entertainment;
          its gastronomy, haute couture, painting, literature, and intellectual
          community especially enjoy an enviable reputation. Its sobriquet “the
          City of Light” (“la Ville Lumière”), earned during the Enlightenment,
          remains appropriate, for Paris has retained its importance as a centre
          for education and intellectual pursuits.

          Paris’s site at a crossroads of both water and land routes significant
          not only to France but also to Europe has had a continuing influence on
          its growth. Under Roman administration, in the 1st century BCE, the original
          site on the Île de la Cité was designated the capital of the Parisii tribe
          and territory. The Frankish king Clovis I had taken Paris from the Gauls
          by 494 CE and later made his capital there. Under Hugh Capet (ruled 987–996)
          and the Capetian dynasty the preeminence of Paris was firmly established,
          and Paris became the political and cultural hub as modern France took shape.
          France has long been a highly centralized country, and Paris has come to
          be identified with a powerful central state, drawing to itself much of
          the talent and vitality of the provinces.

          The three main parts of historical Paris are defined by the Seine. At
          its centre is the Île de la Cité, which is the seat of religious and
          temporal authority (the word cité connotes the nucleus of the ancient city).
          The Seine’s Left Bank (Rive Gauche) has traditionally been the seat of
          intellectual life, and its Right Bank (Rive Droite) contains the heart
          of the city’s economic life, but the distinctions have become blurred
          in recent decades. The fusion of all these functions at the centre of
          France and, later, at the centre of an empire, resulted in a tremendously
          vital environment. In this environment, however, the emotional and
          intellectual climate that was created by contending powers often set the
          stage for great violence in both the social and political arenas—the
          years 1358, 1382, 1588, 1648, 1789, 1830, 1848, and 1871 being notable
          for such events.

          In its centuries of growth Paris has for the most part retained the
          circular shape of the early city. Its boundaries have spread outward to
          engulf the surrounding towns (bourgs), usually built around monasteries
          or churches and often the site of a market. From the mid-14th to the mid-16th
          century, the city’s growth was mainly eastward; since then it has been
          westward. It comprises 20 arrondissements (municipal districts), each
          of which has its own mayor, town hall, and particular features. The
          numbering begins in the heart of Paris and continues in the spiraling
          shape of a snail shell, ending to the far east. Parisians refer to the
          arrondissements by number as the first (premier), second (deuxième),
          third (troisième), and so on. Adaptation to the problems of urbanization—such
          as immigration, housing, social infrastructure, public utilities,
          suburban development, and zoning—has produced the vast urban agglomeration.</p>

